def load(p: Path) -> dict:
    return json.loads(p.read_text())


def _avg(pl: list[dict], path: list[str]) -> float:
    vals = []
    for p in pl:
        x = p
        try:
            for k in path:
                x = x[k]
            vals.append(x)
        except KeyError:
            pass
    return statistics.mean(vals) if vals else float("nan")


