def row_from_petri(name: str, display: str, d: int, n_layers: int) -> list[float]:
    a = petri[name]["avg"]
    return [display, d, n_layers, a["srank"], a["bonus_R_k5"], a["bonus_R_ksrank"], a["bonus_L_k5"]]


def row_from_gamma(runs: dict, run_key: str, display: str, d_in: int, n_layers: int) -> list:
    pl = runs[run_key]["per_layer"]
    return [
        display, d_in, n_layers,
        _avg(pl, ["srank_delta"]),
        _avg(pl, ["k5", "bonus_right"]),
        _avg(pl, ["k_srank", "bonus_right"]),
        _avg(pl, ["k5", "bonus_left"]),
    ]


